// Helper function to get image with fallback
export const getImage = (category, key) => {
  try {
    return IMAGES[category]?.[key] || IMAGES.fallbacks.default;
  } catch (error) {
    console.warn(`Image not found: ${category}.${key}, using fallback`);
    return IMAGES.fallbacks.default;
  }
};

// Helper function to get random image from category
export const getRandomImage = (category) => {
  try {
    const categoryImages = IMAGES[category];
    if (!categoryImages) return IMAGES.fallbacks.default;
    
    const keys = Object.keys(categoryImages);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    return categoryImages[randomKey];
  } catch (error) {
    console.warn(`Failed to get random image from ${category}, using fallback`);
    return IMAGES.fallbacks.default;
  }
};

// Helper function to get learning path images
export const getLearningPathImages = (pathCount) => {
  try {
    const allImages = {
      ...IMAGES.learningPaths.workspace,
      ...IMAGES.learningPaths.contentCreator,
      ...IMAGES.learningPaths.technology,
      ...IMAGES.learningPaths.faceless
    };
    
    const imageKeys = Object.keys(allImages);
    const selectedImages = [];
    
    // Ensure no duplicates and get diverse images
    for (let i = 0; i < pathCount && i < imageKeys.length; i++) {
      const randomIndex = Math.floor(Math.random() * imageKeys.length);
      const selectedImage = allImages[imageKeys[randomIndex]];
      
      if (!selectedImages.includes(selectedImage)) {
        selectedImages.push(selectedImage);
      }
    }
    
    return selectedImages;
  } catch (error) {
    console.warn('Failed to get learning path images, using fallbacks');
    return Array(pathCount).fill(IMAGES.fallbacks.default);
  }
};

