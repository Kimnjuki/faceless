// Helper function to get image by category and key
export function getImage(category: string, key: string): string {
  try {
    const categoryImages = IMAGES[category as keyof typeof IMAGES];
    if (categoryImages && typeof categoryImages === 'object') {
      const image = (categoryImages as any)[key];
      return image || IMAGES.fallbacks.default;
    }
    return IMAGES.fallbacks.default;
  } catch (error) {
    console.warn(`Failed to get image from ${category}.${key}, using fallback`);
    return IMAGES.fallbacks.default;
  }
}

// Helper function to get random image from category
export function getRandomImage(category: string): string {
  try {
    const categoryImages = IMAGES[category as keyof typeof IMAGES];
    if (categoryImages && typeof categoryImages === 'object') {
      const keys = Object.keys(categoryImages as any);
      const randomKey = keys[Math.floor(Math.random() * keys.length)];
      return (categoryImages as any)[randomKey] || IMAGES.fallbacks.default;
    }
    return IMAGES.fallbacks.default;
  } catch (error) {
    console.warn(`Failed to get random image from ${category}, using fallback`);
    return IMAGES.fallbacks.default;
  }
}

// Helper function to get learning path images
export function getLearningPathImages(pathCount: number): string[] {
  try {
    const allImages = {
      ...IMAGES.learningPaths.workspace,
      ...IMAGES.learningPaths.contentCreator,
      ...IMAGES.learningPaths.technology,
      ...IMAGES.learningPaths.faceless
    };
    
    const keys = Object.keys(allImages);
    const shuffled = keys.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, pathCount).map(key => allImages[key as keyof typeof allImages]);
  } catch (error) {
    console.warn('Failed to get learning path images, using fallbacks');
    return Array(pathCount).fill(IMAGES.fallbacks.default);
  }
}

